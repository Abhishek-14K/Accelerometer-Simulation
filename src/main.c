/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */
  /* Infinite loop */
  for(;;)
  {
	  HCSR04_Read(); // Function to get ultrasonic sensor reading
	  dist = filter(real_dist); // Filter out extreme distances
	  if (dist < min_dist && dist != 0) // Calculate minimum distance
	  {
		  min_dist = dist;
	  }
	  if (dist > max_dist) // Calculate maximum distance
	  {
		  max_dist = dist;
	  }


	  serv1 = 130  + (((30-130)*(dist-50))/(1200-50)); // Map servo value based on distance
	  TIM2 -> CCR1 = serv1; // Move servo


	  msdelay(50); // Delay for 50 ms
  }
  /* USER CODE END 5 */
}

