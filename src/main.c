uint16_t filter(uint16_t c) // 3 point median filter to filter out extreme values
{
	uint16_t middle;

	 if ((a <= b) && (a <= c))
	 {
	   middle = (b <= c) ? b : c;
	 }
	 else if ((b <= a) && (b <= c))
	 {
	   middle = (a <= c) ? a : c;
	 }
	 else
	 {
	   middle = (a <= b) ? a : b;
	 }

	 a = b;
	 b = c;
	 return middle;
}

